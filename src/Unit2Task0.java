public class Unit2Task0 {
    public static void main(String[] args){
        int x=1;
        while(x!=11){
            System.out.print(x+" ");
            x++;
        }
        x=1;
        System.out.println();
        do {
            System.out.print(x+" ");
            x++;
        }while(x!=11);
        System.out.println();
        int n=11;
        for(int i=1;i<n;i++){
            System.out.print(i+" ");
        }
    }
}
